    def _init_buffers(self):
        """ Initialize torch tensors which will contain simulation states and processed quantities
        """
        # get gym GPU state tensors
        actor_root_state = self.gym.acquire_actor_root_state_tensor(self.sim)
        dof_state_tensor = self.gym.acquire_dof_state_tensor(self.sim)
        net_contact_forces = self.gym.acquire_net_contact_force_tensor(self.sim)
        self.gym.refresh_dof_state_tensor(self.sim)
        self.gym.refresh_actor_root_state_tensor(self.sim)
        self.gym.refresh_net_contact_force_tensor(self.sim)

        # create some wrapper tensors for different slices
        self.root_states = gymtorch.wrap_tensor(actor_root_state)
        self.dof_state = gymtorch.wrap_tensor(dof_state_tensor)
        self.dof_pos = self.dof_state.view(self.num_envs, self.num_dof, 2)[..., 0]
        self.dof_vel = self.dof_state.view(self.num_envs, self.num_dof, 2)[..., 1]
        self.base_quat = self.root_states[:, 3:7]
        self.rpy = get_euler_xyz_in_tensor(self.base_quat)
        self.base_pos = self.root_states[:self.num_envs, 0:3]
        self.contact_forces = gymtorch.wrap_tensor(net_contact_forces).view(self.num_envs, -1, 3) # shape: num_envs, num_bodies, xyz axis

        # initialize some data used later on
        self.common_step_counter = 0
        self.extras = {}
        self.noise_scale_vec = self._get_noise_scale_vec(self.cfg)
        self.gravity_vec = to_torch(get_axis_params(-1., self.up_axis_idx), device=self.device).repeat((self.num_envs, 1))
        self.forward_vec = to_torch([1., 0., 0.], device=self.device).repeat((self.num_envs, 1))
        self.torques = torch.zeros(self.num_envs, self.num_actions, dtype=torch.float, device=self.device, requires_grad=False)
        self.p_gains = torch.zeros(self.num_actions, dtype=torch.float, device=self.device, requires_grad=False)
        self.d_gains = torch.zeros(self.num_actions, dtype=torch.float, device=self.device, requires_grad=False)
        self.actions = torch.zeros(self.num_envs, self.num_actions, dtype=torch.float, device=self.device, requires_grad=False)
        self.last_actions = torch.zeros(self.num_envs, self.num_actions, dtype=torch.float, device=self.device, requires_grad=False)
        self.last_dof_vel = torch.zeros_like(self.dof_vel)
        self.last_root_vel = torch.zeros_like(self.root_states[:, 7:13])
        self.commands = torch.zeros(self.num_envs, self.cfg.commands.num_commands, dtype=torch.float, device=self.device, requires_grad=False) # x vel, y vel, yaw vel, heading
        self.commands_scale = torch.tensor([self.obs_scales.lin_vel, self.obs_scales.lin_vel, self.obs_scales.ang_vel], device=self.device, requires_grad=False,) # TODO change this
        self.feet_air_time = torch.zeros(self.num_envs, self.feet_indices.shape[0], dtype=torch.float, device=self.device, requires_grad=False)
        self.last_contacts = torch.zeros(self.num_envs, len(self.feet_indices), dtype=torch.bool, device=self.device, requires_grad=False)
        self.base_lin_vel = quat_rotate_inverse(self.base_quat, self.root_states[:, 7:10])
        self.base_ang_vel = quat_rotate_inverse(self.base_quat, self.root_states[:, 10:13])
        self.projected_gravity = quat_rotate_inverse(self.base_quat, self.gravity_vec)
      

        # joint positions offsets and PD gains
        self.default_dof_pos = torch.zeros(self.num_dof, dtype=torch.float, device=self.device, requires_grad=False)
        for i in range(self.num_dofs):
            name = self.dof_names[i]
            angle = self.cfg.init_state.default_joint_angles[name]
            self.default_dof_pos[i] = angle
            found = False
            for dof_name in self.cfg.control.stiffness.keys():
                if dof_name in name:
                    self.p_gains[i] = self.cfg.control.stiffness[dof_name]
                    self.d_gains[i] = self.cfg.control.damping[dof_name]
                    found = True
            if not found:
                self.p_gains[i] = 0.
                self.d_gains[i] = 0.
                if self.cfg.control.control_type in ["P", "V"]:
                    print(f"PD gain of joint {name} were not defined, setting them to zero")
        self.default_dof_pos = self.default_dof_pos.unsqueeze(0)

    def _prepare_reward_function(self):
        """ Prepares a list of reward functions, whcih will be called to compute the total reward.
            Looks for self._reward_<REWARD_NAME>, where <REWARD_NAME> are names of all non zero reward scales in the cfg.
        """
        # remove zero scales + multiply non-zero ones by dt
        for key in list(self.reward_scales.keys()):
            scale = self.reward_scales[key]
            if scale==0:
                self.reward_scales.pop(key) 
            else:
                self.reward_scales[key] *= self.dt
        # prepare list of functions
        self.reward_functions = []
        self.reward_names = []
        for name, scale in self.reward_scales.items():
            if name=="termination":
                continue
            self.reward_names.append(name)
            name = '_reward_' + name
            self.reward_functions.append(getattr(self, name))

        # reward episode sums
        self.episode_sums = {name: torch.zeros(self.num_envs, dtype=torch.float, device=self.device, requires_grad=False)
                             for name in self.reward_scales.keys()}

    def _create_ground_plane(self):
        """ Adds a ground plane to the simulation, sets friction and restitution based on the cfg.
        """
        plane_params = gymapi.PlaneParams()
        plane_params.normal = gymapi.Vec3(0.0, 0.0, 1.0)
        plane_params.static_friction = self.cfg.terrain.static_friction
        plane_params.dynamic_friction = self.cfg.terrain.dynamic_friction
        plane_params.restitution = self.cfg.terrain.restitution
        self.gym.add_ground(self.sim, plane_params)

    def _create_envs(self):
        """ Creates environments:
             1. loads the robot URDF/MJCF asset,
             2. For each environment
                2.1 creates the environment, 
                2.2 calls DOF and Rigid shape properties callbacks,
                2.3 create actor with these properties and add them to the env
             3. Store indices of different bodies of the robot
        """
        asset_path = self.cfg.asset.file.format(LEGGED_GYM_ROOT_DIR=LEGGED_GYM_ROOT_DIR)
        asset_root = os.path.dirname(asset_path)
        asset_file = os.path.basename(asset_path)

        asset_options = gymapi.AssetOptions()
        asset_options.default_dof_drive_mode = self.cfg.asset.default_dof_drive_mode
        asset_options.collapse_fixed_joints = self.cfg.asset.collapse_fixed_joints
        asset_options.replace_cylinder_with_capsule = self.cfg.asset.replace_cylinder_with_capsule
        asset_options.flip_visual_attachments = self.cfg.asset.flip_visual_attachments
        asset_options.fix_base_link = self.cfg.asset.fix_base_link
        asset_options.density = self.cfg.asset.density
        asset_options.angular_damping = self.cfg.asset.angular_damping
        asset_options.linear_damping = self.cfg.asset.linear_damping
        asset_options.max_angular_velocity = self.cfg.asset.max_angular_velocity
        asset_options.max_linear_velocity = self.cfg.asset.max_linear_velocity
        asset_options.armature = self.cfg.asset.armature
        asset_options.thickness = self.cfg.asset.thickness
        asset_options.disable_gravity = self.cfg.asset.disable_gravity

        robot_asset = self.gym.load_asset(self.sim, asset_root, asset_file, asset_options)
        self.num_dof = self.gym.get_asset_dof_count(robot_asset)
        self.num_bodies = self.gym.get_asset_rigid_body_count(robot_asset)
        dof_props_asset = self.gym.get_asset_dof_properties(robot_asset)
        rigid_shape_props_asset = self.gym.get_asset_rigid_shape_properties(robot_asset)

        # save body names from the asset
        body_names = self.gym.get_asset_rigid_body_names(robot_asset)
        self.dof_names = self.gym.get_asset_dof_names(robot_asset)
        self.num_bodies = len(body_names)
        self.num_dofs = len(self.dof_names)
        feet_names = [s for s in body_names if self.cfg.asset.foot_name in s]
        penalized_contact_names = []
        for name in self.cfg.asset.penalize_contacts_on:
            penalized_contact_names.extend([s for s in body_names if name in s])
        termination_contact_names = []
        for name in self.cfg.asset.terminate_after_contacts_on:
            termination_contact_names.extend([s for s in body_names if name in s])

        base_init_state_list = self.cfg.init_state.pos + self.cfg.init_state.rot + self.cfg.init_state.lin_vel + self.cfg.init_state.ang_vel
        self.base_init_state = to_torch(base_init_state_list, device=self.device, requires_grad=False)
        start_pose = gymapi.Transform()
        start_pose.p = gymapi.Vec3(*self.base_init_state[:3])

        self._get_env_origins()
        env_lower = gymapi.Vec3(0., 0., 0.)
        env_upper = gymapi.Vec3(0., 0., 0.)
        self.actor_handles = []
        self.envs = []
        for i in range(self.num_envs):
            # create env instance
            env_handle = self.gym.create_env(self.sim, env_lower, env_upper, int(np.sqrt(self.num_envs)))
            pos = self.env_origins[i].clone()
            pos[:2] += torch_rand_float(-1., 1., (2,1), device=self.device).squeeze(1)
            start_pose.p = gymapi.Vec3(*pos)
                
            rigid_shape_props = self._process_rigid_shape_props(rigid_shape_props_asset, i)
            self.gym.set_asset_rigid_shape_properties(robot_asset, rigid_shape_props)
            actor_handle = self.gym.create_actor(env_handle, robot_asset, start_pose, self.cfg.asset.name, i, self.cfg.asset.self_collisions, 0)
            dof_props = self._process_dof_props(dof_props_asset, i)
            self.gym.set_actor_dof_properties(env_handle, actor_handle, dof_props)
            body_props = self.gym.get_actor_rigid_body_properties(env_handle, actor_handle)
            body_props = self._process_rigid_body_props(body_props, i)
            self.gym.set_actor_rigid_body_properties(env_handle, actor_handle, body_props, recomputeInertia=True)
            self.envs.append(env_handle)
            self.actor_handles.append(actor_handle)

        self.feet_indices = torch.zeros(len(feet_names), dtype=torch.long, device=self.device, requires_grad=False)
        for i in range(len(feet_names)):
            self.feet_indices[i] = self.gym.find_actor_rigid_body_handle(self.envs[0], self.actor_handles[0], feet_names[i])

        self.penalised_contact_indices = torch.zeros(len(penalized_contact_names), dtype=torch.long, device=self.device, requires_grad=False)
        for i in range(len(penalized_contact_names)):
            self.penalised_contact_indices[i] = self.gym.find_actor_rigid_body_handle(self.envs[0], self.actor_handles[0], penalized_contact_names[i])

        self.termination_contact_indices = torch.zeros(len(termination_contact_names), dtype=torch.long, device=self.device, requires_grad=False)
        for i in range(len(termination_contact_names)):
            self.termination_contact_indices[i] = self.gym.find_actor_rigid_body_handle(self.envs[0], self.actor_handles[0], termination_contact_names[i])

    def _get_env_origins(self):
        """ Sets environment origins. On rough terrain the origins are defined by the terrain platforms.
            Otherwise create a grid.
        """
      
        self.custom_origins = False
        self.env_origins = torch.zeros(self.num_envs, 3, device=self.device, requires_grad=False)
        # create a grid of robots
        num_cols = np.floor(np.sqrt(self.num_envs))
        num_rows = np.ceil(self.num_envs / num_cols)
        xx, yy = torch.meshgrid(torch.arange(num_rows), torch.arange(num_cols))
        spacing = self.cfg.env.env_spacing
        self.env_origins[:, 0] = spacing * xx.flatten()[:self.num_envs]
        self.env_origins[:, 1] = spacing * yy.flatten()[:self.num_envs]
        self.env_origins[:, 2] = 0.

    def _parse_cfg(self, cfg):
        self.dt = self.cfg.control.decimation * self.sim_params.dt
        self.obs_scales = self.cfg.normalization.obs_scales
        self.reward_scales = class_to_dict(self.cfg.rewards.scales)
        self.command_ranges = class_to_dict(self.cfg.commands.ranges)
     

        self.max_episode_length_s = self.cfg.env.episode_length_s
        self.max_episode_length = np.ceil(self.max_episode_length_s / self.dt)

        self.cfg.domain_rand.push_interval = np.ceil(self.cfg.domain_rand.push_interval_s / self.dt)
        self._init_domain_rand_curriculum()

    def _to_range_pair(self, value, fallback):
        if value is None:
            value = fallback
        if isinstance(value, (list, tuple, np.ndarray)):
            if len(value) >= 2:
                low, high = float(value[0]), float(value[1])
            elif len(value) == 1:
                low = high = float(value[0])
            else:
                low, high = float(fallback[0]), float(fallback[1])
        else:
            low = high = float(value)
        if low > high:
            low, high = high, low
        return [low, high]

    def _interpolate_range(self, nominal, max_range, level):
        max_low, max_high = self._to_range_pair(max_range, [nominal, nominal])
        cur_low = nominal - level * (nominal - max_low)
        cur_high = nominal + level * (max_high - nominal)
        if cur_low > cur_high:
            cur_low, cur_high = cur_high, cur_low
        return [float(cur_low), float(cur_high)]

    def get_curriculum_level(self):
        return float(self.cdr_level)

    def set_curriculum_level(self, level):
        self.cdr_level = float(np.clip(level, 0.0, self.cdr_max_level))
        self.compute_current_domain_rand_ranges()

    def compute_current_domain_rand_ranges(self):
        interpolation_level = float(np.clip(self.cdr_level, 0.0, 1.0))

        if self.cdr_use_stagewise_progression:
            param_levels = {}
            for idx, param_name in enumerate(self.cdr_stage_order):
                if idx < self.cdr_stage_idx:
                    param_levels[param_name] = 1.0
                elif idx == self.cdr_stage_idx:
                    param_levels[param_name] = interpolation_level
                else:
                    param_levels[param_name] = 0.0
        else:
            param_levels = {key: interpolation_level for key in self.cdr_stage_order}

        self.domain_rand_current_ranges = {
            "friction_range": self._interpolate_range(
                self.domain_rand_nominal["friction"],
                self.domain_rand_max_ranges["friction_range"],
                param_levels["friction"],
            ),
            "added_mass_range": self._interpolate_range(
                self.domain_rand_nominal["added_mass"],
                self.domain_rand_max_ranges["added_mass_range"],
                param_levels["added_mass"],
            ),
            "push_vel_xy_range": self._interpolate_range(
                self.domain_rand_nominal["push_vel_xy"],
                self.domain_rand_max_ranges["push_vel_xy_range"],
                param_levels["push_vel_xy"],
            ),
        }
        # Keep existing config keys in sync for backward compatibility with legacy code paths.
        self.cfg.domain_rand.friction_range = self.domain_rand_current_ranges["friction_range"]
        self.cfg.domain_rand.added_mass_range = self.domain_rand_current_ranges["added_mass_range"]
        push_low, push_high = self.domain_rand_current_ranges["push_vel_xy_range"]
        self.cfg.domain_rand.max_push_vel_xy = max(abs(push_low), abs(push_high))
        return self.domain_rand_current_ranges

    def sample_domain_randomization(self, env_ids=None):
        if env_ids is None:
            env_ids_np = np.arange(self.num_envs, dtype=np.int32)
        elif isinstance(env_ids, torch.Tensor):
            env_ids_np = env_ids.detach().cpu().numpy().astype(np.int32).reshape(-1)
        else:
            env_ids_np = np.asarray(env_ids, dtype=np.int32).reshape(-1)

        if env_ids_np.size == 0:
            return

        if self.cfg.domain_rand.randomize_friction:
            friction_low, friction_high = self.domain_rand_current_ranges["friction_range"]
            sampled_friction = np.random.uniform(friction_low, friction_high, size=env_ids_np.size).astype(np.float32)
            if not hasattr(self, "friction_coeffs") or self.friction_coeffs.shape[0] != self.num_envs:
                self.friction_coeffs = torch.zeros((self.num_envs, 1), dtype=torch.float, device='cpu')

            for env_id, friction in zip(env_ids_np, sampled_friction):
                env_index = int(env_id)
                self.friction_coeffs[env_index, 0] = float(friction)
                shape_props = self.gym.get_actor_rigid_shape_properties(self.envs[env_index], self.actor_handles[env_index])
                for prop in shape_props:
                    prop.friction = float(friction)
                self.gym.set_actor_rigid_shape_properties(self.envs[env_index], self.actor_handles[env_index], shape_props)

        if self.cfg.domain_rand.randomize_base_mass and hasattr(self, "base_body_masses"):
            mass_low, mass_high = self.domain_rand_current_ranges["added_mass_range"]
            sampled_added_mass = np.random.uniform(mass_low, mass_high, size=env_ids_np.size).astype(np.float32)
            for env_id, added_mass in zip(env_ids_np, sampled_added_mass):
                env_index = int(env_id)
                self.added_mass_offsets[env_index] = float(added_mass)
                body_props = self.gym.get_actor_rigid_body_properties(self.envs[env_index], self.actor_handles[env_index])
                body_props[0].mass = float(self.base_body_masses[env_index] + added_mass)
                self.gym.set_actor_rigid_body_properties(self.envs[env_index], self.actor_handles[env_index], body_props, recomputeInertia=True)

    def _metric_to_float(self, value):
        if value is None:
            return None
        if isinstance(value, torch.Tensor):
            if value.numel() == 0:
                return None
            return float(value.detach().mean().item())
        try:
            return float(value)
        except (TypeError, ValueError):
            return None

    def update_curriculum(self, metrics_dict):
        info = {
            "updated": False,
            "level": self.get_curriculum_level(),
            "stage": int(self.cdr_stage_idx),
        }
        if not self.cdr_enabled:
            return info

        metrics_dict = metrics_dict or {}
        self.cdr_metrics_window.append(metrics_dict)
        self.cdr_iteration_counter += 1

        if self.cdr_iteration_counter % self.cdr_update_interval != 0:
            return info

        window = list(self.cdr_metrics_window)

        def weighted_mean(key):
            weighted_sum = 0.0
            weight_sum = 0.0
            for metrics in window:
                metric_value = self._metric_to_float(metrics.get(key))
                if metric_value is None:
                    continue
                metric_weight = self._metric_to_float(metrics.get("num_episodes"))
                if metric_weight is None:
                    metric_weight = 1.0
                if metric_weight <= 0.0:
                    continue
                weighted_sum += metric_value * metric_weight
                weight_sum += metric_weight
            if weight_sum <= 0.0:
                return None
            return weighted_sum / weight_sum

        num_episodes = 0.0
        for metrics in window:
            metric_weight = self._metric_to_float(metrics.get("num_episodes"))
            if metric_weight is not None and metric_weight > 0.0:
                num_episodes += metric_weight

        success_rate = weighted_mean("success_rate")
        if success_rate is None:
            fall_rate = weighted_mean("fall_rate")
            if fall_rate is not None:
                success_rate = 1.0 - fall_rate

        avg_reward = weighted_mean("mean_reward")
        tracking_reward = weighted_mean("tracking_reward")

        info["num_episodes"] = num_episodes
        info["success_rate"] = success_rate
        info["mean_reward"] = avg_reward
        info["tracking_reward"] = tracking_reward

        if num_episodes < self.cdr_min_episodes_for_update:
            return info
        if success_rate is None or success_rate <= self.cdr_success_threshold:
            return info

        previous_level = self.get_curriculum_level()
        previous_stage = int(self.cdr_stage_idx)

        target_level = min(previous_level + self.cdr_level_step, self.cdr_max_level)
        stage_advanced = False
        if self.cdr_use_stagewise_progression and target_level >= self.cdr_max_level and self.cdr_stage_idx < len(self.cdr_stage_order) - 1:
            self.cdr_stage_idx += 1
            stage_advanced = True
            stage_reset_level = float(getattr(self.cfg.domain_rand.cdr, "initial_level", 0.0))
            target_level = float(np.clip(stage_reset_level, 0.0, self.cdr_max_level))

        self.set_curriculum_level(target_level)

        info["updated"] = stage_advanced or (abs(self.get_curriculum_level() - previous_level) > 1e-8)
        info["stage_advanced"] = stage_advanced
        info["previous_level"] = previous_level
        info["previous_stage"] = previous_stage
        info["level"] = self.get_curriculum_level()
        info["stage"] = int(self.cdr_stage_idx)
        info["friction_range"] = self.domain_rand_current_ranges["friction_range"]
        info["added_mass_range"] = self.domain_rand_current_ranges["added_mass_range"]
        info["push_vel_xy_range"] = self.domain_rand_current_ranges["push_vel_xy_range"]
        return info

    def _init_domain_rand_curriculum(self):
        cdr_cfg = getattr(self.cfg.domain_rand, "cdr", None)
        self.cdr_enabled = bool(getattr(cdr_cfg, "enabled", False)) if cdr_cfg is not None else False
        self.cdr_max_level = max(0.0, float(getattr(cdr_cfg, "max_level", 1.0))) if cdr_cfg is not None else 1.0
        initial_level = float(getattr(cdr_cfg, "initial_level", self.cdr_max_level)) if cdr_cfg is not None else 1.0
        self.cdr_level_step = max(0.0, float(getattr(cdr_cfg, "level_step", 0.0))) if cdr_cfg is not None else 0.0
        self.cdr_update_interval = max(1, int(getattr(cdr_cfg, "update_interval", 1))) if cdr_cfg is not None else 1
        self.cdr_success_threshold = float(getattr(cdr_cfg, "success_threshold", 1.0)) if cdr_cfg is not None else 1.0
        self.cdr_min_episodes_for_update = max(1, int(getattr(cdr_cfg, "min_episodes_for_update", 1))) if cdr_cfg is not None else 1
        self.cdr_use_stagewise_progression = bool(getattr(cdr_cfg, "use_stagewise_progression", False)) if cdr_cfg is not None else False
        self.cdr_stage_order = ["friction", "added_mass", "push_vel_xy"]
        self.cdr_stage_idx = 0
        self.cdr_metrics_window = deque(maxlen=self.cdr_update_interval)
        self.cdr_iteration_counter = 0

        if self.cdr_enabled:
            self.cdr_level = float(np.clip(initial_level, 0.0, self.cdr_max_level))
        else:
            self.cdr_level = float(np.clip(self.cdr_max_level, 0.0, 1.0))

        fallback_friction_max = self._to_range_pair(getattr(self.cfg.domain_rand, "friction_range", [1.0, 1.0]), [1.0, 1.0])
        fallback_added_mass_max = self._to_range_pair(getattr(self.cfg.domain_rand, "added_mass_range", [0.0, 0.0]), [0.0, 0.0])
        fallback_push_max = float(getattr(self.cfg.domain_rand, "max_push_vel_xy", 0.0))
        fallback_push_vel_max = [-fallback_push_max, fallback_push_max]

        friction_max = self._to_range_pair(getattr(self.cfg.domain_rand, "friction_max_range", fallback_friction_max), fallback_friction_max)
        added_mass_max = self._to_range_pair(getattr(self.cfg.domain_rand, "added_mass_max_range", fallback_added_mass_max), fallback_added_mass_max)
        push_vel_max = self._to_range_pair(getattr(self.cfg.domain_rand, "push_vel_xy_max_range", fallback_push_vel_max), fallback_push_vel_max)

        self.domain_rand_nominal = {
            "friction": float(getattr(self.cfg.domain_rand, "friction_nominal", 0.5 * (friction_max[0] + friction_max[1]))),
            "added_mass": float(getattr(self.cfg.domain_rand, "added_mass_nominal", 0.0)),
            "push_vel_xy": float(getattr(self.cfg.domain_rand, "push_vel_xy_nominal", 0.0)),
        }
        self.domain_rand_max_ranges = {
            "friction_range": friction_max,
            "added_mass_range": added_mass_max,
            "push_vel_xy_range": push_vel_max,
        }
        self.compute_current_domain_rand_ranges()

    def _set_domain_rand_level(self, level):
        self.set_curriculum_level(level)


    #------------ reward functions----------------
    def _reward_lin_vel_z(self):
        # Penalize z axis base linear velocity
        return torch.square(self.base_lin_vel[:, 2])
    
    def _reward_ang_vel_xy(self):
        # Penalize xy axes base angular velocity
        return torch.sum(torch.square(self.base_ang_vel[:, :2]), dim=1)
    
    def _reward_orientation(self):
        # Penalize non flat base orientation
        return torch.sum(torch.square(self.projected_gravity[:, :2]), dim=1)

    def _reward_base_height(self):
        # Penalize base height away from target
        base_height = self.root_states[:, 2]
        return torch.square(base_height - self.cfg.rewards.base_height_target)
    
    def _reward_torques(self):
        # Penalize torques
        return torch.sum(torch.square(self.torques), dim=1)

    def _reward_dof_vel(self):
        # Penalize dof velocities
        return torch.sum(torch.square(self.dof_vel), dim=1)
    
    def _reward_dof_acc(self):
        # Penalize dof accelerations
        return torch.sum(torch.square((self.last_dof_vel - self.dof_vel) / self.dt), dim=1)
    
    def _reward_action_rate(self):
        # Penalize changes in actions
        return torch.sum(torch.square(self.last_actions - self.actions), dim=1)
    
    def _reward_collision(self):
        # Penalize collisions on selected bodies
        return torch.sum(1.*(torch.norm(self.contact_forces[:, self.penalised_contact_indices, :], dim=-1) > 0.1), dim=1)
    
    def _reward_termination(self):
        # Terminal reward / penalty
        return self.reset_buf * ~self.time_out_buf
    
    def _reward_dof_pos_limits(self):
        # Penalize dof positions too close to the limit
        out_of_limits = -(self.dof_pos - self.dof_pos_limits[:, 0]).clip(max=0.) # lower limit
        out_of_limits += (self.dof_pos - self.dof_pos_limits[:, 1]).clip(min=0.)
        return torch.sum(out_of_limits, dim=1)

    def _reward_dof_vel_limits(self):
        # Penalize dof velocities too close to the limit
        # clip to max error = 1 rad/s per joint to avoid huge penalties
        return torch.sum((torch.abs(self.dof_vel) - self.dof_vel_limits*self.cfg.rewards.soft_dof_vel_limit).clip(min=0., max=1.), dim=1)

    def _reward_torque_limits(self):
        # penalize torques too close to the limit
        return torch.sum((torch.abs(self.torques) - self.torque_limits*self.cfg.rewards.soft_torque_limit).clip(min=0.), dim=1)

    def _reward_tracking_lin_vel(self):
        # Tracking of linear velocity commands (xy axes)
        lin_vel_error = torch.sum(torch.square(self.commands[:, :2] - self.base_lin_vel[:, :2]), dim=1)
        return torch.exp(-lin_vel_error/self.cfg.rewards.tracking_sigma)
    
    def _reward_tracking_ang_vel(self):
        # Tracking of angular velocity commands (yaw) 
        ang_vel_error = torch.square(self.commands[:, 2] - self.base_ang_vel[:, 2])
        return torch.exp(-ang_vel_error/self.cfg.rewards.tracking_sigma)

    def _reward_feet_air_time(self):
        # Reward long steps
        # Need to filter the contacts because the contact reporting of PhysX is unreliable on meshes
        contact = self.contact_forces[:, self.feet_indices, 2] > 1.
        contact_filt = torch.logical_or(contact, self.last_contacts) 
        self.last_contacts = contact
        first_contact = (self.feet_air_time > 0.) * contact_filt
        self.feet_air_time += self.dt
        rew_airTime = torch.sum((self.feet_air_time - 0.5) * first_contact, dim=1) # reward only on first contact with the ground
        rew_airTime *= torch.norm(self.commands[:, :2], dim=1) > 0.1 #no reward for zero command
        self.feet_air_time *= ~contact_filt
        return rew_airTime
    
    def _reward_stumble(self):
        # Penalize feet hitting vertical surfaces
        return torch.any(torch.norm(self.contact_forces[:, self.feet_indices, :2], dim=2) >\
             5 *torch.abs(self.contact_forces[:, self.feet_indices, 2]), dim=1)
        
    def _reward_stand_still(self):
        # Penalize motion at zero commands
        return torch.sum(torch.abs(self.dof_pos - self.default_dof_pos), dim=1) * (torch.norm(self.commands[:, :2], dim=1) < 0.1)

    def _reward_feet_contact_forces(self):
        # penalize high contact forces
        return torch.sum((torch.norm(self.contact_forces[:, self.feet_indices, :], dim=-1) -  self.cfg.rewards.max_contact_force).clip(min=0.), dim=1)
